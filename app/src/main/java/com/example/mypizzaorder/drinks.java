package com.example.mypizzaorder;

public class drinks {
    double drink_size_price=0;
    double Quarter_price=0;
    double Half_price=0;
    double Litre_price=0;


    public drinks() {

    }

    public double getDrink_size_price() {
        return drink_size_price;
    }

    public void setDrink_size_price(double drink_size_price) {
        this.drink_size_price = drink_size_price;
    }

    public double getQuarter_price() {
        return Quarter_price;
    }

    public void setQuarter_price(double quarter_price) {
        Quarter_price = quarter_price;
    }

    public double getHalf_price() {
        return Half_price;
    }

    public void setHalf_price(double half_price) {
        Half_price = half_price;
    }

    public double getLitre_price() {
        return Litre_price;
    }

    public void setLitre_price(double litre_price) {
        Litre_price = litre_price;
    }
}
